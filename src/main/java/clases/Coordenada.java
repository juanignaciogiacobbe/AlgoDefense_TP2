package clases;

public class Coordenada {
    final int abscisa;
    final int ordenada;

    public Coordenada(int UnaAbscisa, int unaOrdenada) {
        this.abscisa = UnaAbscisa;
        this.ordenada = unaOrdenada;
    }

    public int getAbscisa() {
        return this.abscisa;
    }

    public int getOrdenada() {
        return this.ordenada;
    }


}
